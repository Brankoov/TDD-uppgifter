package se.branko;

public class Calculator {

    public double add(double a, double b) {
        return a + b;
    }
    public double sub(double a, double b) {
        return a - b;
    }
    public double multi(double a, double b) {
        return a * b;
    }
    public double divide(double a, double b) {
        return a / b;
    }
}
